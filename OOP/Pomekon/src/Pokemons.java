public class Pokemons {
    
    private int ata;
    private int def;
    private int level;

    public int getAta(){
        return ata;
    }

    public void setAta(int ata){
        this.ata = ata;
    }

    public int getDef(){
        return def;
    }

    public void setDef(int def){
        this.def = def;
    }

    public int getLevel(){
        return level;
    }

    public void setLevel(int level){
        this.level = level;
    }

}
